<b>Berapa lama durasi pengiriman untuk COD?</b><br>
Estimasi pengiriman untuk COD adalah 2-3 hari kerja.
<br><br>
<b>Daerah mana saja yang dijangkau oleh service COD?</b><br>
Jabodetabek, Bandung, Surabaya, dan Medan.
<br><br>
<b>Apakah bisa menggunakan Debit Card atau Credit Card saat COD?</b><br>
Ya. Pembayaran menggunakan Debit Card semua Bank dapat digunakan. Credit Card hanya yang berlogo Visa dan Mastercard. Tidak ada biaya tambahannya. Minimum pembelian Rp 200.000.
<br><br>
<b>Apa yang dimaksud dengan COD (Cash on Delivery)?</b><br>
COD (Cash on Delivery) adalah metode pembayaran di mana Customer membayar barang yang dipesan pada saat barang diterima oleh Customer.
<br><br>
<b>Apa yang harus saya lakukan jika saya ingin menukar/mengembalikan barang?</b><br>
Mohon sign in ke MoslemLifestyle.com dengan menggunakan akunmu, lalu buka halaman 'Confirm/track order'. Jika kamu ingin mengembalikan pesananmu, klik 'Return'. Pilih barang yang ingin kamu kembalikan dan pastikan kamu memilih pengembalian uang. Kami hanya dapat memproses pengembalian dengan perincian tersebut.Barang yang dikembalikan kepada kami harus dalam kondisi original, BELUM DIPAKAI, DIUBAH, DICUCI, dan masih lengkap dengan label/pricetag nya. Jika kamu ingin menukar barang, mohon hubungi Customer Service kami. Penukaran barang hanya berlaku untuk barang dengan warna/ukuran yang berbeda pada barang yang sama. Kami tidak menerima penukaran barang karena kamu tidak menyukai model/style pada barang tersebut.
Pastikan kamu telah membaca Kebijakan Penukaran/Pengembalian Barang kami.
<br><br>
<b>Apakah MoslemLifestyle.com membuka kerjasama dengan sistem reseller/dropship?</b><br>
Tidak, untuk saat ini kami belum membuka kerjasama dengan sistem reseller/dropship.
<br><br>
<b>Bagaimana cara menjadi tenant di MoslemLifestyle.com?</b><br>
Untuk menjadi tenant di MoslemLifestyle.com, silakan isi Form Pendaftaran Tenant. Jika brand Kamu lolos seleksi maka kami akan menghubungimu
<br><br>
<b>Apa perbedaan antara alamat saya dan alamat pengiriman?</b><br>
Alamat pengiriman adalah alamat di mana kamu menginginkan pesanan kamu dikirim. Ketika kamu mengisi alamat pribadimu pada akun Moslem Lifestyle, alamat tersebut akan menjadi alamat pengiriman. Tetapi jika kamu ingin pesanan dikirim ke alamat lain, kamu dapat mengubah alamat pengiriman pada form rincian pengiriman.
<br><br>
<b>Apakah MoslemLifestyle.com melayani pemesanan internasional?</b><br>
Ya kami melayani pemesanan internasional dengan menggunakan jasa EMS Pos Indonesia. Cara pemesanannya mirip seperti pemesanan domestik, hanya saja sebelum memesan, Kamu perlu memilih "English" sebagai bahasa. Untuk informasi lebih lengkap silakan hubungi customer@MoslemLifestyle.com.
<br><br>
<b>Pesanan saya belum sampai.</b><br>
Pastikan kamu telah mengecek estimasi barang sampai pada perincian pesanan yang ada pada email kamu. Jika pesananmu belum sampai hingga melewati batas estimasi sampainya barang, mohon hubungi Customer Service kami.
